                <p>Since 1950 half Britain’s hedges have been lost, with farmers even subsidised to bulldoze them out of existence. Happily by the 1990s the value of this integral and essential part of our countryside, including some that are centuries old and with distinctive regional features, has been realised. Since 1997 it has been illegal to remove a hedge over 30 years old without planning permission.</p>

                <p>Remarkably, around half a million miles of hedges contain 15% of our  stock of native trees and are a haven for a myriad of wildflowers,  invertebrates, birds and mammals. However, hedges which also provide for stock proofing, fodder, wind shelter and land demarcation, require  active management. Hedge laying (plashing) is particularly suited for tall outgrown hedges when stems are partially cut through at an angle near the base, bent over horizontally and woven between regularly placed stakes. This stimulates growth, re-establishes a barrier and improves the wildlife habitat. Replanting mainly with hawthorn and hazel is also necessary where there has been overgrowth by species of trees that cannot be readily be laid. These processes have been applied and can be readily now be seen close to town on the Gobowen Road by Old Oswestry where the Cambrian Heritage Railways has a community orchard. The video shows Allan Housman,  local traditional hedge layer, and Tom Adams, local orchardist, at work  on Oswestry’s community orchard site.</p>

