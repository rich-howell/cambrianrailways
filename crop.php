                <p>Location - Our one acre site is located between the railway line and Gobowen Road (B5069), an arterial route passing Oswestry’s Iron Age Hill Fort, and is accessible from the town on foot or bike where we work a regular weekly Friday morning session throughout the year.</strong></p>

                <p><strong>Phase 1</strong> Started in November 2011,and was completed within the year, fulfilling several of our aims, namely: building the foundations of a traditional orchard working with local craftsmen eg. on fencing and hedge laying planting half standard fruiting trees including local varieties (apple, pear, plum/gauge and cherry) grafting fruit trees in the nearby nursery of local organic orchardist <a>Tom Adams</a> maintaining direct linkage and contributing to the ‘green corridor’ of the railway providing experience in the development of a sustainable and bio-diverse environment. Work started with litter and ground clearance, followed by felling of dead elm trees, chipping, green composting, herb and soft fruit planting and wildlife habitat building.</p>

                <p><strong>Phase 2</strong> This began in January 2013 with redevelopment of the site entrance, completion of the shelter, planting 50 more apple trees as cordons, 15 cherry trees to be grown as fans, construction of a soft fruit nursery bed of blackcurrant, redcurrant and gooseberry and the planting of herbs. The number of established fruit trees is now 140. A hive of British honey bees was established in Autumn 2013 by local beekeeper Richard Lewis who offers training in apiary techniques, and from which we extracted our first honey in September 2014. Another hive was added in 2015 by Pam Scott who has been taught by Richard. A shipping container for tool storage was provided by the Railway in May 2014.</p>

                <p><strong>Phase 3</strong> This involves deciduous woodland tree planting to the south of the entrance area with a view to seasoning, coppicing and charcoal production.</p>

                <p><strong>Courses and Events</strong> Courses have been run for hedge laying, hedge planting and also the grafting, planting and pruning of fruit trees. Training was also given during the building of a wooden shelter which now provides cover in all weathers and captures rainwater which runs into 5 thousand-litre tanks in case of drought. Wassailing has been held in the January of 2014-16 whilst June 2016 saw our first formal schools visit, followed by a guided evening Herb Walk in July.</p>

                <p><strong>Biodiversity</strong> A biodiversity study of the site and neighbouring railway cutting was undertaken in August 2013 by the Shropshire Council Biodiversity Officer. Observations on wildflowers, birds, butterflies, bumble bees and fungi are recorded regularly by volunteers and submissions on observations have been made to the British Bumble Bee Conservation Trust (University of Stirling), the annual RSPB (January) and Big Butterfly (July) Counts, and in 2015 the BBC and Woodland Trust’s <em>Springwatch</em> programme.</p>

                <p><strong>Produce</strong> 2014 was the first time that a significant number of trees and bushes yielded fruit. This was given to the Food Bank and/or volunteers. In 2015 we also began to sell fruit locally to raise revenue to sustain the project and have also sold felled elm from the site for firewood.</p>

                <p><strong>Volunteers</strong> Most adult volunteers have come to the project via the Qube volunteer scheme, other community support organisations eg. Enable and NACRO, Community Payback Scheme, with the remainder by word of mouth. Youth volunteers have come to CROP through County Training and New Century Court in Oswestry, and Brynmelyn and Amberleigh Residential Care groups. Several adult and youth volunteers have gone on to gainful employment elsewhere.</p>

                <p><strong>Record Keeping</strong> Risk assessments and attendance sheets are routinely completed and a works diary is kept. To date, work have been undertaken by over 50 adults and 40 youth volunteers.</p>

                <p><strong>Finance</strong> An <em>‘Awards for All’</em> HLF Grant of £10k was achieved in 2011 allowing us to start the project. Further small grants for the project have been awarded by the Naturesave Trust (2013) for the purchase of a mower, Shropshire Council with DEFRA and RHS (2014 and 2015) for the purchase of mulberry and nut trees, tools and publicity, Shropshire Towns and Rural Housing Community Chest Funding to build a compost toilet during 2015 and rabbit proof fencing in 2016 and the Woodland Trust (2017) for native trees to develop woodland area.</p>
            
